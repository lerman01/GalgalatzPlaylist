package galgalatz.youtube.playlist.galgalatz;

public class GalgalatzResponse {
	private String title;
	private String autor;
	private String titleNext;
	private String autorNext;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getAutor() {
		return autor;
	}

	public void setAutor(String autor) {
		this.autor = autor;
	}

	public String getTitleNext() {
		return titleNext;
	}

	public void setTitleNext(String titleNext) {
		this.titleNext = titleNext;
	}

	public String getAutorNext() {
		return autorNext;
	}

	public void setAutorNext(String autorNext) {
		this.autorNext = autorNext;
	}

}
